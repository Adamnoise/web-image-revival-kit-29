
import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "minimal" | "outline" | "glass"
  }
>(({ className, variant = "default", ...props }, ref) => {
  const baseStyles = "rounded-lg border bg-card text-card-foreground shadow-sm";
  
  const variantStyles = {
    default: baseStyles,
    glass: "rounded-lg bg-black/40 border border-white/10 shadow-sm",
    minimal: "rounded-lg bg-[#111] border border-white/10 text-white",
    outline: "rounded-lg bg-transparent border border-white/20 text-white",
  }
  
  return (
    <div
      ref={ref}
      className={cn(
        variantStyles[variant],
        className
      )}
      {...props}
    />
  )
})
Card.displayName = "Card"

const CardHeader = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => {
  return (
    <div
      ref={ref}
      className={cn("flex flex-col space-y-1.5 p-6", className)}
      {...props}
    />
  )
})
CardHeader.displayName = "CardHeader"

